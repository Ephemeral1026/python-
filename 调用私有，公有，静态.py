class Fruit:
    price=0
    def __init__(self):
        self.color='Red'
        self.city='Kunming'
        def __outputColor(self):
            print(self.__color)
        def __outputCity(self):
            print(self.__city)
        def output(self):
            self.__outputColor()
            self.__outputCity()
        @ staticmethod
        def getPrice():
            return Fruit.price
        @ staticmethod
        def setPrice(p):
            Fruit.price=p


                
